/**
 * Cache for dynamic translations to avoid repeated API requests, persisting across reloads.
 */
let translationCache: Record<string, string> = {};
try {
  const cached = localStorage.getItem("puima_ai_translations");
  if (cached) {
    translationCache = JSON.parse(cached);
  }
} catch (e) {
  console.warn("Could not load translation cache:", e);
}

/**
 * Clean on-the-fly translation of Korean to English using Gemini API
 */
export async function translateTextWithAI(text: string): Promise<string> {
  if (!text || !text.trim()) return text;
  
  const trimmed = text.trim();

  // Try exact lookup from manual dictionary first
  if (translationDictionary[trimmed] !== undefined) {
    return translationDictionary[trimmed];
  }

  // Try static rules
  for (const [key, val] of Object.entries(translationDictionary)) {
    if (trimmed === key) return val;
  }

  // Try cache lookup
  if (translationCache[trimmed]) {
    return translationCache[trimmed];
  }

  try {
    const response = await fetch("/api/translate", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({ text: trimmed }),
    });

    if (response.ok) {
      const data = await response.json();
      const translated = data.translated?.trim();
      if (translated) {
        translationCache[trimmed] = translated;
        try {
          localStorage.setItem("puima_ai_translations", JSON.stringify(translationCache));
        } catch (e) {}
        return translated;
      }
    }
  } catch (err) {
    console.error("Server API translation of notice failed:", err);
  }

  return trimmed;
}

/**
 * Translates a text if the active language is ENG.
 */
export function translate(text: string, lang: "KOR" | "ENG"): string {
  if (lang === "KOR" || !text) {
    return text;
  }
  
  const trimmed = text.trim();
  
  // Try exact lookup
  if (translationDictionary[trimmed]) {
    return translationDictionary[trimmed];
  }

  // Handle common sentences/paragraphs with dynamic contents
  for (const [key, value] of Object.entries(translationDictionary)) {
    if (trimmed.includes(key)) {
      return trimmed.replace(key, value);
    }
  }

  // Fallback to original text if no translation found
  return text;
}

